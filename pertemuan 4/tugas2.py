A = input("masukkan nilai A : ")

B = input("masukkan nilai B : ")

penjumlahan = (int(A) + int(B))
pengurangan = (int(A) - int(B))
perkalian = (int(A) * int(B))
pembagian = (int(A) / int(B))
modulus = (int(A) % int(B))

print("hasil penjumlahan dari " + str(A) + " dan " + str(B) + " adalah " + str(penjumlahan))
print("hasil pengurangan dari " + str(A) + " dan " + str(B) + " adalah " + str(pengurangan))
print("hasil perkalian dari " + str(A) + " dan " + str(B) + " adalah " + str(perkalian))
print("hasil pembagian dari " + str(A) + " dan " + str(B) + " adalah " + str(pembagian))
print("hasil modulus dari " + str(A) + " dan " + str(B) + " adalah " + str(modulus))

